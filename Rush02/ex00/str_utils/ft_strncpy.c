#include "rush02.h"

void	ft_strncpy(char *dest, char *src, int n)
{
	while (*src && n)
	{
		*dest++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*dest++ = '\0';
		n--;
	}
}
